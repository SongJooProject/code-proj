import os

import docx
from docx.enum.text import WD_ALIGN_PARAGRAPH


def create_quiz_file(filename, questions, answers):
    """문제/답안 docx 파일 생성"""

    # 문제 파일 생성
    doc_question = docx.Document()

    # 제목
    title = doc_question.add_heading(f"{filename} 문제", level=1)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # 문제 목록
    for i, q in enumerate(questions, 1):
        doc_question.add_paragraph(f"{i}. {q['question']}")

        # 선택지
        for j, choice in enumerate(q["choices"], 1):
            doc_question.add_paragraph(f"  ({j}) {choice}")

        doc_question.add_paragraph("")  # 빈 줄

    # 문제 파일 저장
    question_filename = f"{filename}_문제.docx"
    doc_question.save(os.path.join(os.path.dirname(__file__), question_filename))

    # 답안 파일 생성
    doc_answer = docx.Document()

    # 제목
    title = doc_answer.add_heading(f"{filename} 답안", level=1)
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER

    # 답안 목록
    for i, (_q, a) in enumerate(zip(questions, answers, strict=True), 1):
        doc_answer.add_paragraph(f"{i}. 정답: {a['answer']}")
        doc_answer.add_paragraph(f"   설명: {a['explanation']}")
        doc_answer.add_paragraph("")

    # 답안 파일 저장
    answer_filename = f"{filename}_답안.docx"
    doc_answer.save(os.path.join(os.path.dirname(__file__), answer_filename))

    print(f"생성 완료: {question_filename}, {answer_filename}")


